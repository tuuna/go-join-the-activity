        var new_activity=document.querySelector(".new_activity");
        function hover(a){
            a.style.color="#4EA1E7";
        }
        function link(b) {
            b.style.color="#4A4A4A";
        }
        function change_hot(){
            new_activity.style.display='none';
            hot_activity.style.display='block';
            link(new_btn);
            hover(hot_btn);
        }
        function change_new(){
            hot_activity.style.display='none';
            new_activity.style.display='block';
            hover(new_btn);
            link(hot_btn);
        }
        new_btn.addEventListener('click',change_new,false);
        hot_btn.addEventListener('click',change_hot,false);
